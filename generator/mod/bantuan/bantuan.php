	1. First make the desired menu as the place where the CRUD will be run, it can be the Master menu, Transactions, Reports or others.<br/>
	2. To make CRUD first make a table that will be used as a transaction.<br/>
	3. Select the CRUD Generator menu, select the table that CRUD will create, fill in the title and description.<br/>
	4. Then set the Module Settings so that the CRUD is created according to the link.<br/>
	5. All application will be created in folder apps/pages.<br/>
	6. To change database name you can open folder apps/lib with conn.php as filename.
